"""
Xception V1 model for Keras.

Optionally loads weights pre-trained on ImageNet. This model is available for TensorFlow only, and 
can only be used with inputs following the TensorFlow data format `(width, height, channels)`. You 
have to set `image_data_format="channels_last"` in your Keras config located at ~/.keras/keras.json.
On ImageNet, this model gets to a top-1 validation accuracy of 0.790 and a top-5 validation accuracy
of 0.945.

Do note that the input image format for this model is different than for the VGG16 and ResNet models 
(299x299 instead of 224x224),and that the input preprocessing function is also different (same as 
Inception V3).

Also do note that this model is only available for the TensorFlow backend, due to its reliance on 
`SeparableConvolution` layers.

Reference:
[Xception: Deep Learning with Depthwise Separable Convolutions](https://arxiv.org/abs/1610.02357)
"""


import warnings
import numpy as np
from keras.preprocessing import image
from keras.models import Model

from keras import backend as K
from keras import layers
from keras.layers import Input, Conv2D, SeparableConv2D, Dense, Activation, BatchNormalization, \
    MaxPooling2D, GlobalAveragePooling2D, GlobalMaxPooling2D

from keras.engine.topology import get_source_inputs
from keras.utils.data_utils import get_file
from keras.applications.imagenet_utils import decode_predictions
from imagenet_utils import _obtain_input_shape


